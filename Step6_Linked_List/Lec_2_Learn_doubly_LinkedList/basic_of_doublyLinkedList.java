package Step6_Linked_List.Lec_2_Learn_doubly_LinkedList;

 class Node{
    int data;
    Node next;
    Node prev;

    Node(int datas){
        this.data = datas;
        this.next = null;
        this.prev = null;
    }
    Node(int datas , Node next , Node prev){
        this.data = datas;
        this.next = next;
        this.prev  = prev;
    }
}

